class If_Else{
    public static void main(String args[]){
        int num1=15;
        int num2=20;
        if(num1<10 && num2>15){
            System.out.println("Inside If");
        }
        else if(num2<20 || num1>10){
            System.out.println("Inside Else If");
        }
        else{
            System.out.println("Inside Else");
        }
        
        
    }
}
